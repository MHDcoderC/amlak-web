// Icon wrapper component for consistent sizing
export const IconWrapper = ({ icon, size = 20, className = '', ...props }) => {
  const Component = icon;
  return <Component size={size} className={className} {...props} />;
};

// Property type icon component
export const PropertyTypeIcon = ({ type, size = 20, className = '' }) => {
  const config = PropertyTypeIcons[type] || PropertyTypeIcons.apartment;
  const Icon = config.icon;
  return <Icon size={size} className={`${config.color} ${className}`} />;
};

// Status icon component
export const StatusIcon = ({ status, size = 20, className = '' }) => {
  const config = StatusIcons[status] || StatusIcons.pending;
  const Icon = config.icon;
  return <Icon size={size} className={`${config.color} ${className}`} />;
};
